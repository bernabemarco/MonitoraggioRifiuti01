CREATE VIEW A_VIS_REGIVA AS
SELECT NRREG,NRRIGA ,DATAREG,causale,NUMDOC,DATADOC,NRRIFER,ANNOLIQUIDAZ,MESELIQUIDAZ,NRREGISTRO,TIPOREGISTROIVA.DESCRIZIONE DSCREG,TESTECONTABILITA.ESERCIZIO,TIPOREGISTROIVA.CODICE CODTIPOREG,MesePlafond,AnnoPlafond,TrattamentoIva.Codice CODIVA,TESTECONTABILITA.NrGiornale
 FROM TESTECONTABILITA
     LEFT JOIN RIGHEIVA ON TESTECONTABILITA.PROGRESSIVO=RIGHEIVA.NRREG 
	 LEFT JOIN CAUSALICONTABILI ON TESTECONTABILITA.CAUSALE=CAUSALICONTABILI.CODICECAUSALE 
	 INNER JOIN TIPOREGISTROIVA ON RIGHEIVA.TIPOREGISTRO=TIPOREGISTROIVA.CODICE 
     LEFT OUTER JOIN TRATTAMENTOIVA ON RIGHEIVA.CODIVA=TRATTAMENTOIVA.CODICE 

GO
