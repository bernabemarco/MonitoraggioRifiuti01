
CREATE view [dbo].[ViewArticoliOF]
AS
	SELECT 
		AA.CODICE,
		AA.GRUPPO,
		AA.CATEGORIA,
		AA.CODCATEGORIASTAT,
		AAC.GRUPPOPRZPART,
		TPE.CodFamigliaPos,
		TPE.CodRepartoPos
	FROM 
		ANAGRAFICAARTICOLI AA,
		ANAGRAFICAARTICOLICOMM AAC,
		TP_EXTRAMAG TPE
	WHERE
		AAC.CODICEART = AA.CODICE AND
		AAC.ESERCIZIO = (SELECT ESERCIZIOATTIVO FROM TABUTENTI WHERE USERDB = USER_NAME()) AND
		TPE.CODART = AA.CODICE AND
		TPE.CONSIDACQ = 1



GO
GRANT SELECT
    ON OBJECT::[dbo].[ViewArticoliOF] TO [Metodo98]
    AS [dbo];

