
CREATE VIEW TP_VISTARIGHEDOCDACHIUDEREMISS AS

	SELECT DISTINCT
           TESTEDOCUMENTI.PROGRESSIVO AS PROGRESSDOC,             
		   TESTEDOCUMENTI.TIPODOC,TESTEDOCUMENTI.ESERCIZIO,
		   TESTEDOCUMENTI.NUMERODOC,TESTEDOCUMENTI.NUMRIFDOC,TESTEDOCUMENTI.BIS,
		   TESTEDOCUMENTI.DATADOC,TESTEDOCUMENTI.CODCLIFOR,ANAGRAFICACF.DSCCONTO1,
		   TESTEDOCUMENTI.NUMDESTDIVERSAMERCI,TESTEDOCUMENTI.RAGSOCDDM,
		   TESTEDOCUMENTI.INDIRIZZODDM,TESTEDOCUMENTI.CAPDDM,TESTEDOCUMENTI.LOCALITADDM,TESTEDOCUMENTI.PROVINCIADDM,
		   TabDocMissNonEvadibili.PROGRMISS,TabDocMissNonEvadibili.IDRIGADOC,TabDocMissNonEvadibili.CODART,TabDocMissNonEvadibili.DESCART,TabDocMissNonEvadibili.UM,TabDocMissNonEvadibili.QTAORD,TabDocMissNonEvadibili.DATACONSEGNA        
	 FROM TabDocMissNonEvadibili
	 INNER JOIN TESTEDOCUMENTI ON TabDocMissNonEvadibili.PROGDOC=TESTEDOCUMENTI.PROGRESSIVO
	 INNER JOIN ANAGRAFICACF ON TESTEDOCUMENTI.CODCLIFOR=ANAGRAFICACF.CODCONTO
 	 WHERE
	  TabDocMissNonEvadibili.RIGACHIUSA=1



GO
