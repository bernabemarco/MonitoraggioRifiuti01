



CREATE VIEW CONTRATTIRIGHEDOCUMENTI
AS
SELECT     dbo.RIGHEDOCUMENTI.*, dbo.ARTICOLIFATTORICONVERSIONE.UM1 AS UM1, dbo.ARTICOLIFATTORICONVERSIONE.UM2 AS Um2, 
                      dbo.RIGHEDOCUMENTI.QTAGEST * dbo.ARTICOLIFATTORICONVERSIONE.FATTORE AS QTAGESTCONVERTITA
FROM         dbo.RIGHEDOCUMENTI INNER JOIN
                      dbo.ARTICOLIFATTORICONVERSIONE ON dbo.RIGHEDOCUMENTI.CODART = dbo.ARTICOLIFATTORICONVERSIONE.CODART AND 
                      dbo.RIGHEDOCUMENTI.UMGEST = dbo.ARTICOLIFATTORICONVERSIONE.UM1


GO
