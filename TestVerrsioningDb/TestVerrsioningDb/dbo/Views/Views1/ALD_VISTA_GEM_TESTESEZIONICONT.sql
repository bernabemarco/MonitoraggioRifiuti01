AS

SELECT     GEM_SEZIONICONTRATTO.IDCONTRATTO, GEM_SEZIONICONTRATTO.SEZIONECONTRATTO, GEM_TESTACONTRATTO.CODCLIENTE, 
                      GEM_SEZIONICONTRATTO.CODDEST
