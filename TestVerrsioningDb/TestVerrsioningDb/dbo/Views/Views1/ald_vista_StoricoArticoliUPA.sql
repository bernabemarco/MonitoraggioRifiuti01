
CREATE VIEW [dbo].[ald_vista_StoricoArticoliUPA]
AS
SELECT     ANAGRAFICAARTICOLI.CODICE AS CodArt, ANAGRAFICAARTICOLI.DESCRIZIONE AS DscArticolo, ISNULL(STORICOPREZZIARTICOLO.PREZZOEURO, 0) 
                      AS ValoreUPA
FROM         ANAGRAFICAARTICOLI LEFT OUTER JOIN
                      STORICOPREZZIARTICOLO ON ANAGRAFICAARTICOLI.CODICE = STORICOPREZZIARTICOLO.CODARTICOLO
WHERE     (isnull(STORICOPREZZIARTICOLO.TIPOPREZZO, 'F') = 'F') AND (isnull(STORICOPREZZIARTICOLO.ULTIMO, 1) = 1)

GO
GRANT SELECT
    ON OBJECT::[dbo].[ald_vista_StoricoArticoliUPA] TO [Metodo98]
    AS [dbo];

