
CREATE VIEW [dbo].[VISTACONSUNTIVATOGRUPPOACQUISTO]
AS
SELECT     dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.PROGRESSIVO, dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.ESERCIZIO, 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.CODCLIFOR, dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.GRUPPOACQ, 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.MESE, dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.ANNO, 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.ARTICOLO AS CODART, dbo.CONTRATTI_DETTCONSUNTIVGRUPPIACQ.IDSOCIO AS ASSOCIATO, 
                      dbo.CONTRATTI_DETTCONSUNTIVGRUPPIACQ.UM, dbo.CONTRATTI_DETTCONSUNTIVGRUPPIACQ.QTA, dbo.CONTRATTI_DETTCONSUNTIVGRUPPIACQ.VALORE, 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.MARCHIO
FROM         dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ INNER JOIN
                      dbo.CONTRATTI_DETTCONSUNTIVGRUPPIACQ ON 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.PROGRESSIVO = dbo.CONTRATTI_DETTCONSUNTIVGRUPPIACQ.RIFPROGRESSIVO INNER JOIN
                      dbo.VISTAMAXMESECONSGRPACQ ON dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.CODCLIFOR = dbo.VISTAMAXMESECONSGRPACQ.CODCLIFOR AND 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.ANNO = dbo.VISTAMAXMESECONSGRPACQ.anno AND 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.MESE = dbo.VISTAMAXMESECONSGRPACQ.MESE AND 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.ESERCIZIO = dbo.VISTAMAXMESECONSGRPACQ.ESERCIZIO AND 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.ARTICOLO = dbo.VISTAMAXMESECONSGRPACQ.ARTICOLO AND 
                      dbo.CONTRATTI_TESTECONSUNTIVGRUPPIACQ.GRUPPOACQ = dbo.VISTAMAXMESECONSGRPACQ.GRUPPOACQ


GO
