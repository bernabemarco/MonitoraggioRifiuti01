
CREATE VIEW dbo.VIEWCONTRATTIAV_TESTE
AS

SELECT     dbo.CONTRATTIAVPROGRESSIVO_TESTE.PROGRESSIVO, dbo.TESTE_CONTRATTI.NUMEROCONTRATTO, 
                      dbo.CONTRATTIAVPROGRESSIVO_TESTE.CONTRATTO, dbo.CONTRATTIAVPROGRESSIVO_TESTE.DADATA, 
                      dbo.CONTRATTIAVPROGRESSIVO_TESTE.ADATA, dbo.TESTE_CONTRATTI.DESCRIZIONECONTRATTO, dbo.TESTE_CONTRATTI.CODICE
FROM         dbo.TESTE_CONTRATTI INNER JOIN
                      dbo.CONTRATTIAVPROGRESSIVO_TESTE ON dbo.TESTE_CONTRATTI.PROGRESSIVO = dbo.CONTRATTIAVPROGRESSIVO_TESTE.CONTRATTO


GO
