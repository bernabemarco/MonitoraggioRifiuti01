
CREATE VIEW FRCVistaArticoliVBMateriali AS 
	SELECT AMBIENTE, ANNODOC, MESEDOC, ANNOMESEDOC, CDRMercato, CDRProdotto, CODART, UMGEST, UMPREZZO, FATTORE, SUM(QTAGEST) AS QTAGEST, SUM(QTAPREZZO) AS QTAPREZZO
	FROM FRCDatiVenditeBudget
	GROUP BY AMBIENTE, ANNODOC, MESEDOC, ANNOMESEDOC, CDRMercato, CDRProdotto, CODART, UMGEST, UMPREZZO, FATTORE

GO
GRANT SELECT
    ON OBJECT::[dbo].[FRCVistaArticoliVBMateriali] TO [Metodo98]
    AS [dbo];

