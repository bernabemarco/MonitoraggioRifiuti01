    ON OBJECT::[dbo].[AIOTCONTRATTICL_MERCE] TO [Metodo98]
    AS [dbo];


GO
GRANT REFERENCES
    ON OBJECT::[dbo].[AIOTCONTRATTICL_MERCE] TO [Metodo98]
    AS [dbo];


GO
