

CREATE VIEW [GEM_Vista_ContrattiRateali]
AS
SELECT     GEM_SEZIONECONTRATTO_RATE.NUMERORATA, GEM_TESTACONTRATTO.IDCONTRATTO, GEM_TESTACONTRATTO.CODCLIENTE, 
                      GEM_TESTACONTRATTO.CODCLIFAT, GEM_TESTACONTRATTO.DATACONTRATTO, GEM_TESTACONTRATTO.DATAEFFICACIA, 
                      GEM_TESTACONTRATTO.DATAPVISITA, GEM_TESTACONTRATTO.DURATA, GEM_TESTACONTRATTO.CodAgente1, 
                      GEM_TESTACONTRATTO.CodAgente3, GEM_TESTACONTRATTO.CodAgente2, GEM_TESTACONTRATTO.FLAG_PROV, 
                      GEM_TESTACONTRATTO.PAGAMENTO, GEM_TESTACONTRATTO.REFERENTE, GEM_TESTACONTRATTO.TEL_REF, GEM_TESTACONTRATTO.IdStato, 
                      GEM_TESTACONTRATTO.DataIdStato, GEM_TESTACONTRATTO.PCONTR, GEM_TESTACONTRATTO.DATA_DIS, GEM_TESTACONTRATTO.RESP_MANU, 
                      GEM_TESTACONTRATTO.RESP_CONTR, GEM_TESTACONTRATTO.SCONTO, GEM_TESTACONTRATTO.DATARACC, 
                      GEM_TESTACONTRATTO.CONTRORIG, GEM_TESTACONTRATTO.MINISFS, GEM_TESTACONTRATTO.FLGUSAPRZGEMMA, 
                      GEM_TESTACONTRATTO.PREAVVISOFAT, GEM_TESTACONTRATTO.flgvendis, GEM_TESTACONTRATTO.flg_mat_dafatt, 
                      GEM_TESTACONTRATTO.FLG_RIC_SCA_AUTO, GEM_TESTACONTRATTO.EMAIL_RESPONSABILE, GEM_TESTACONTRATTO.RIN_PCONTR, 
                      GEM_TESTACONTRATTO.STAGIONALE, GEM_TESTACONTRATTO.PROVV_SEGNALATORE, GEM_TESTACONTRATTO.CODCategoriaContratto, 
                      GEM_TESTACONTRATTO.CODReferenteInternoGestione, GEM_TESTACONTRATTO.RegistroOnLine, GEM_TESTACONTRATTO.RichiestaAutorizzFattura, 
                      GEM_TESTACONTRATTO.CollegatoAdAltriContratti, GEM_TESTACONTRATTO.CondizioniParticolariCollegate, GEM_TESTACONTRATTO.idModFatt, 
                      GEM_TESTACONTRATTO.idTipoFatt, GEM_TESTACONTRATTO.PubblicaGemmaPlus, GEM_TESTACONTRATTO.DataPrimaRata, 
                      GEM_TESTACONTRATTO.CadenzaRate, VISTACLIENTI.STATOALTRO
FROM         GEM_SEZIONECONTRATTO_RATE INNER JOIN
                      GEM_TESTACONTRATTO ON GEM_SEZIONECONTRATTO_RATE.IDCONTRATTO = GEM_TESTACONTRATTO.IDCONTRATTO INNER JOIN
                      VISTACLIENTI ON GEM_TESTACONTRATTO.CODCLIENTE = VISTACLIENTI.CODCONTO

GO
GRANT SELECT
    ON OBJECT::[dbo].[GEM_Vista_ContrattiRateali] TO [Metodo98]
    AS [dbo];

