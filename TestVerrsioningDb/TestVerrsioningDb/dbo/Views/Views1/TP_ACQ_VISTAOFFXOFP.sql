
CREATE VIEW TP_ACQ_VISTAOFFXOFP AS 

SELECT     *
FROM       ACQ_TESTAOFFERTA
WHERE     (FLAGDAFATTURARE = 1) AND (PROGRESSIVO IN
          (SELECT     RifTransferOrder
           FROM       TESTEDOCUMENTI INNER JOIN
                      TP_PRENOTATI ON TP_PRENOTATI.IDTESTAP = TESTEDOCUMENTI.PROGRESSIVO
           WHERE      TP_PRENOTATI.STATO = 'A'))



GO
