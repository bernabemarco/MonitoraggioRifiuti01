
CREATE VIEW VISTA_TESTE_CONTRATTI
AS
SELECT     TESTE_CONTRATTI.PROGRESSIVO, TESTE_CONTRATTI.UTENTEMODIFICA, TESTE_CONTRATTI.DATAMODIFICA, 
                      TESTE_CONTRATTI.ESERCIZIO, TESTE_CONTRATTI.NUMEROCONTRATTO, TESTE_CONTRATTI.BIS, 
                      TESTE_CONTRATTI.DESCRIZIONECONTRATTO, TESTE_CONTRATTI.TIPONODO, TESTE_CONTRATTI.CODICE, 
                      TESTE_CONTRATTI.DATAINIZIOESERCIZIO, TESTE_CONTRATTI.DATAFINEESERCIZIO, TESTE_CONTRATTI.DATAPROPOSTACONTRATTO, 
                      TESTE_CONTRATTI.DATAAPPROVAZIONECONTRATTO, TESTE_CONTRATTI.RESPINTERNI1, TESTE_CONTRATTI.DATAINIZIOCONTRATTO, 
                      TESTE_CONTRATTI.RESPINTERNI2, TESTE_CONTRATTI.DATAFINECONTRATTO, TESTE_CONTRATTI.RESPINTERNI3, 
                      TESTE_CONTRATTI.DATAINTERRUZIONECONTRATTO, TESTE_CONTRATTI.MOTIVAZIONE, TESTE_CONTRATTI.NOTECONTRATTO, 
                      TESTE_CONTRATTI.NODOPARENT, TESTE_CONTRATTI.INDICEALBERO, TESTE_CONTRATTI.NATURA, TESTE_CONTRATTI.LISTINO, 
                      TESTE_CONTRATTI.SCONTO1, TESTE_CONTRATTI.MESEESCL1, TESTE_CONTRATTI.GGESCL1, TESTE_CONTRATTI.MESEESCL2, 
                      TESTE_CONTRATTI.GGESCL2, TESTE_CONTRATTI.APPLSCONTORIGA, TESTE_CONTRATTI.APPLSCONTOFINALE, 
                      TESTE_CONTRATTI.CODCONTOFATT, TESTE_CONTRATTI.CODPAG, TESTE_CONTRATTI.CODGRUPPOPREZZIPART, 
                      TESTE_CONTRATTI.PRCTRASPORTO, TESTE_CONTRATTI.RIVIVAOMAGGI, TESTE_CONTRATTI.USAPRZPRVPART, 
                      TESTE_CONTRATTI.FATTFINEMESE, TESTE_CONTRATTI.NONRAGGRUPPDOC, TESTE_CONTRATTI.SPESEDOCUM, 
                      TESTE_CONTRATTI.SPESEEFFETTO, TESTE_CONTRATTI.SPESEIMBALLO, TESTE_CONTRATTI.SPESETRASPORTO, 
                      TESTE_CONTRATTI.CLIENTE, TESTE_CONTRATTI.FORNITORE, TESTE_CONTRATTI.PERIODOULTIMOCALCAVANZ, 
                      TESTE_CONTRATTI.NOTE_CL, TESTE_CONTRATTI.CONSUNTIVAGRUPPIACQUISTO, TESTE_CONTRATTI.DATASTORICIZZAZIONE, 
                      CASE TESTE_CONTRATTI.TIPONODO WHEN 1 THEN DSCCONTO1 WHEN 2 THEN DSCCONTO1 WHEN 4 THEN DSCCONTO1 WHEN 3 THEN VISTATESTECONTRATTI_DESTDIVERSE.RAGIONESOCIALE
                       ELSE NOMENODO END AS NOMENODO, teste_contratti.fidoeuro, teste_contratti.datainiziofido, teste_contratti.marchio
FROM         TESTE_CONTRATTI LEFT OUTER JOIN
                      VISTATESTECONTRATTI_DESTDIVERSE ON 
                      TESTE_CONTRATTI.PROGRESSIVO = VISTATESTECONTRATTI_DESTDIVERSE.PROGRESSIVO LEFT OUTER JOIN
                      ANAGRAFICACF ON TESTE_CONTRATTI.CODICE = ANAGRAFICACF.CODCONTO



