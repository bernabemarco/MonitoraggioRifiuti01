
CREATE VIEW VISTADESTINAZIONICF AS
SELECT CODCONTO, 0 AS NUMERO, 
    DSCCONTO1 AS RAGIONESOCIALE, 
    DSCCONTO2 AS RAGIONESOCIALE2, INDIRIZZO, CAP, 
    LOCALITA, PROVINCIA, BANCAANAGR, ABI, CAB, 
    CAB + '/' + ABI AS CABABI, FAX, TELEFONO
FROM VISTACLIFOR

GO
GRANT SELECT
    ON OBJECT::[dbo].[VISTADESTINAZIONICF] TO [Metodo98]
    AS [dbo];

