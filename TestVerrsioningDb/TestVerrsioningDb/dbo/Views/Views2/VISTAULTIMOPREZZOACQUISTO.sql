CREATE VIEW VISTAULTIMOPREZZOACQUISTO 
AS
SELECT 
	CODARTICOLO, 
	DATA,  
	CODCLIFOR, 
	UM, 
	QUANTITA, 
	PREZZOLIRE, 
	PREZZOEURO, 
	CODPAGAMENTO 
FROM 
	STORICOPREZZIARTICOLO
WHERE ULTIMO = 1 AND TIPOPREZZO = 'F'

GO
