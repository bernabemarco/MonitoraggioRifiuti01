CREATE VIEW VISTASTAMPASTATIVAREGIONE AS
SELECT RI.*,
   CF.DSCCONTO1,CF.CODMASTRO,CF.CODNAZIONE,CF.CODICEISO,CF.CODREGIONE,CF.PARTITAIVA,
   (SELECT DESCRIZIONE FROM TABREGIONI WHERE CODICE=CF.CODREGIONE) AS DSCREGIONE,
   (SELECT NRGIORNALE FROM TESTECONTABILITA WHERE PROGRESSIVO=RI.NRREG) AS NRGIORNALE,
   (SELECT DATAREG FROM TESTECONTABILITA WHERE PROGRESSIVO=RI.NRREG) AS DATAREG
    FROM RIGHEIVA RI, ANAGRAFICACF CF WHERE RI.CONTO=CF.CODCONTO

GO
GRANT SELECT
    ON OBJECT::[dbo].[VISTASTAMPASTATIVAREGIONE] TO [Metodo98]
    AS [dbo];

