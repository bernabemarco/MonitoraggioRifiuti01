
CREATE VIEW VISTASTORICOMAGMATR AS
SELECT 
	STORICOMAG.PROGRESSIVO AS RIFPROGRESSIVO, 
	STORICOMAGMATRICOLE.CODMATRICOLA,
	STORICOMAG.CODART
	FROM STORICOMAG LEFT OUTER JOIN STORICOMAGMATRICOLE ON 
	STORICOMAG.PROGRESSIVO=STORICOMAGMATRICOLE.PROGRESSIVO

GO
GRANT SELECT
    ON OBJECT::[dbo].[VISTASTORICOMAGMATR] TO [Metodo98]
    AS [dbo];

