
CREATE VIEW VISTAULTIMOPREZZOVENDITA 
AS
SELECT 
	CODARTICOLO, 
	DATA, 
	CODCLIFOR, 
	UM, 
	QUANTITA, 
	PREZZOLIRE, 
	PREZZOEURO, 
	CODPAGAMENTO 
FROM 
	STORICOPREZZIARTICOLO
WHERE ULTIMO = 1 AND TIPOPREZZO = 'C'

GO
GRANT SELECT
    ON OBJECT::[dbo].[VISTAULTIMOPREZZOVENDITA] TO [Metodo98]
    AS [dbo];

