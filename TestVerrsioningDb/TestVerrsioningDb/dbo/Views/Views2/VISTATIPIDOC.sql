
CREATE VIEW VISTATIPIDOC AS
        SELECT DISTINCT TIPO, 
        (CASE WHEN TIPO='B' THEN 'BOLLA' ELSE(CASE WHEN TIPO='O' THEN 'ORDINE' ELSE(CASE WHEN TIPO='F' THEN 'FATTURA' ELSE(CASE WHEN TIPO='N' THEN 'NOTA ACCREDITO' ELSE 'ALTRO' END)  END)  END)  END)  AS DESCRIZIONE
        FROM PARAMETRIDOC

GO
GRANT SELECT
    ON OBJECT::[dbo].[VISTATIPIDOC] TO [Metodo98]
    AS [dbo];

