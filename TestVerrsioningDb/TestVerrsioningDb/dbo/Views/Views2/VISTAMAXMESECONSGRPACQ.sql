
CREATE VIEW VISTAMAXMESECONSGRPACQ
AS
SELECT     VISTAMAXANNOCONSGRPACQ.CODCLIFOR, VISTAMAXANNOCONSGRPACQ.ESERCIZIO, VISTAMAXANNOCONSGRPACQ.anno, 
                      MAX(CONTRATTI_TESTECONSUNTIVGRUPPIACQ.MESE) AS MESE, VISTAMAXANNOCONSGRPACQ.ARTICOLO, 
                      VISTAMAXANNOCONSGRPACQ.GRUPPOACQ
FROM         VISTAMAXANNOCONSGRPACQ INNER JOIN
                      CONTRATTI_TESTECONSUNTIVGRUPPIACQ ON 
                      VISTAMAXANNOCONSGRPACQ.CODCLIFOR = CONTRATTI_TESTECONSUNTIVGRUPPIACQ.CODCLIFOR AND 
                      VISTAMAXANNOCONSGRPACQ.anno = CONTRATTI_TESTECONSUNTIVGRUPPIACQ.ANNO AND 
                      VISTAMAXANNOCONSGRPACQ.ESERCIZIO = CONTRATTI_TESTECONSUNTIVGRUPPIACQ.ESERCIZIO AND 
                      VISTAMAXANNOCONSGRPACQ.ARTICOLO = CONTRATTI_TESTECONSUNTIVGRUPPIACQ.ARTICOLO AND 
                      VISTAMAXANNOCONSGRPACQ.GRUPPOACQ = CONTRATTI_TESTECONSUNTIVGRUPPIACQ.GRUPPOACQ
GROUP BY VISTAMAXANNOCONSGRPACQ.CODCLIFOR, VISTAMAXANNOCONSGRPACQ.anno, VISTAMAXANNOCONSGRPACQ.ESERCIZIO, 
                      VISTAMAXANNOCONSGRPACQ.ARTICOLO, VISTAMAXANNOCONSGRPACQ.GRUPPOACQ


GO
