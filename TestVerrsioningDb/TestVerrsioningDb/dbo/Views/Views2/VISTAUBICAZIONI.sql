CREATE VIEW VISTAUBICAZIONI AS
SELECT DISTINCT
	UA.CODICEART,
	UD.CODDEPOSITO,
	UD.CODUBICAZIONE,
	UD.DESCRIZIONE AS DESCRIZIONEUBI
FROM
	TABUBICAZIONIDEPOSITI UD LEFT OUTER JOIN UBICAZIONIARTICOLI UA ON UD.CODUBICAZIONE = UA.CODUBICAZIONE

GO
GRANT SELECT
    ON OBJECT::[dbo].[VISTAUBICAZIONI] TO [Metodo98]
    AS [dbo];

