
CREATE VIEW VISTASITUAZIONEFASI AS
SELECT 
    VSF.*, EXT.*
FROM
    VISTASITUAZIONEFASIBASE VSF INNER JOIN VISTASITUAZIONEFASI_EXT EXT ON 
    VSF.NOMEPIANIF=EXT.NOMEPIANIF_J AND VSF.PROG_ID=EXT.PROG_ID_J AND VSF.FASE_ID=EXT.FASE_ID_J AND VSF.TIPO=EXT.TIPO_J

GO
