/*  INSERT TRIGGER "TI_DISTINTABASE" FOR TABLE "DISTINTABASE"  */
CREATE TRIGGER TI_DISTINTABASE ON DISTINTABASE FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "DISTINTAARTCOMPOSTI" MUST EXIST WHEN INSERTING A CHILD IN "DISTINTABASE"  */
    IF UPDATE(RIFPROGRESSIVO)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   DISTINTAARTCOMPOSTI T1, INSERTED T2
           WHERE  T1.PROGRESSIVO = T2.RIFPROGRESSIVO) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "DISTINTAARTCOMPOSTI". Cannot create child in "DISTINTABASE".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

