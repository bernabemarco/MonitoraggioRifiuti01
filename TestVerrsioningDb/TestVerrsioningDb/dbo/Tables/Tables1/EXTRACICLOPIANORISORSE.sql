            AND   T1.PROGRESSIVO = T2.PROGRESSIVO) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "TESTACICLOPIANORISORSE". Cannot create child in "EXTRACICLOPIANORISORSE".'
