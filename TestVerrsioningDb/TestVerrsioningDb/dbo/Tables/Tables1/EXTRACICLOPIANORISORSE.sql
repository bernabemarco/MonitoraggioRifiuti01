BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "TESTACICLOPIANORISORSE" MUST EXIST WHEN INSERTING A CHILD IN "EXTRACICLOPIANORISORSE"  */
    IF UPDATE(NOMEPIANORIS) OR
       UPDATE(PROGRESSIVO)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   TESTACICLOPIANORISORSE T1, INSERTED T2
