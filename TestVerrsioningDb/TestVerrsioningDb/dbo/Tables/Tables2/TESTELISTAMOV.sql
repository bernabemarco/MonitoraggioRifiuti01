/*  INSERT TRIGGER "TI_TESTELISTAMOV" FOR TABLE "TESTELISTAMOV"  */
CREATE TRIGGER TI_TESTELISTAMOV ON TESTELISTAMOV FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "PARAMETRILISTEPROD" MUST EXIST WHEN INSERTING A CHILD IN "TESTELISTAMOV"  */
    IF UPDATE(CODICELISTA)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   PARAMETRILISTEPROD T1, INSERTED T2
           WHERE  T1.CODICE = T2.CODICELISTA) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "PARAMETRILISTEPROD". Cannot create child in "TESTELISTAMOV".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

/*  UPDATE TRIGGER "TU_TESTELISTAMOV" FOR TABLE "TESTELISTAMOV"  */
CREATE TRIGGER TU_TESTELISTAMOV ON TESTELISTAMOV FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      
      /*  PARENT "PARAMETRILISTEPROD" MUST EXIST WHEN UPDATING A CHILD IN "TESTELISTAMOV"  */
      IF UPDATE(CODICELISTA)
      BEGIN
         IF (SELECT COUNT(*)
             FROM   PARAMETRILISTEPROD T1, INSERTED T2
             WHERE  T1.CODICE = T2.CODICELISTA) != @NUMROWS
            BEGIN
               SELECT @ERRNO  = 30003,
                      @ERRMSG = '"PARAMETRILISTEPROD" does not exist. Cannot ALTER COLUMN child in "TESTELISTAMOV".'
               GOTO ERROR
            END
      END
      
      /*  ALTER COLUMN PARENT CODE OF "TESTELISTAMOV" FOR ALL CHILDREN IN "RIGHELISTAMOV"  */
      IF UPDATE(PROGRESSIVO)
      BEGIN
         UPDATE RIGHELISTAMOV
          SET   IDTESTA = I1.PROGRESSIVO
         FROM   RIGHELISTAMOV T2, INSERTED I1, DELETED D1
         WHERE  T2.IDTESTA = D1.PROGRESSIVO
          AND  (I1.PROGRESSIVO != D1.PROGRESSIVO)
      END

      RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

/*  DELETE TRIGGER "TD_TESTELISTAMOV" FOR TABLE "TESTELISTAMOV"  */
CREATE TRIGGER TD_TESTELISTAMOV ON TESTELISTAMOV FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  DELETE ALL CHILDREN IN "RIGHELISTAMOV"  */
    DELETE RIGHELISTAMOV
    FROM   RIGHELISTAMOV T2, DELETED T1
    WHERE  T2.IDTESTA = T1.PROGRESSIVO

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

