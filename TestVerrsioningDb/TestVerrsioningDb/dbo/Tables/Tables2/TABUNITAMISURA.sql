                    WHERE  T2.UM = D1.CODICE
                     AND  (I1.CODICE != D1.CODICE))
            BEGIN
               SELECT @ERRNO  = 30005,
                      @ERRMSG = 'Children still exist in "ARTICOLIUNITAMISURA". Cannot modify parent code in "TABUNITAMISURA".'
               GOTO ERROR
            END
      END
      
      /*  CANNOT MODIFY PARENT CODE IN "TABUNITAMISURA" IF CHILDREN STILL EXIST IN "LISTINIARTICOLI"  */
      IF UPDATE(CODICE)
      BEGIN
         IF EXISTS (SELECT 1
                    FROM   LISTINIARTICOLI T2, INSERTED I1, DELETED D1
                    WHERE  T2.UM = D1.CODICE
                     AND  (I1.CODICE != D1.CODICE))
            BEGIN
               SELECT @ERRNO  = 30005,
                      @ERRMSG = 'Children still exist in "LISTINIARTICOLI". Cannot modify parent code in "TABUNITAMISURA".'
               GOTO ERROR
