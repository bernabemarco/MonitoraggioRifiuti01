/*  INSERT TRIGGER "TI_TESTELISTATRASF" FOR TABLE "TESTELISTATRASF"  */
CREATE TRIGGER TI_TESTELISTATRASF ON TESTELISTATRASF FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "PARAMETRILISTETRASF" MUST EXIST WHEN INSERTING A CHILD IN "TESTELISTATRASF"  */
    IF UPDATE(CODICELISTA)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   PARAMETRILISTETRASF T1, INSERTED T2
           WHERE  T1.CODICE = T2.CODICELISTA) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "PARAMETRILISTETRASF". Cannot create child in "TESTELISTATRASF".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

/*  UPDATE TRIGGER "TU_TESTELISTATRASF" FOR TABLE "TESTELISTATRASF"  */
CREATE TRIGGER TU_TESTELISTATRASF ON TESTELISTATRASF FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      
      /*  PARENT "PARAMETRILISTETRASF" MUST EXIST WHEN UPDATING A CHILD IN "TESTELISTATRASF"  */
      IF UPDATE(CODICELISTA)
      BEGIN
         IF (SELECT COUNT(*)
             FROM   PARAMETRILISTETRASF T1, INSERTED T2
             WHERE  T1.CODICE = T2.CODICELISTA) != @NUMROWS
            BEGIN
               SELECT @ERRNO  = 30003,
                      @ERRMSG = '"PARAMETRILISTETRASF" does not exist. Cannot ALTER COLUMN child in "TESTELISTATRASF".'
               GOTO ERROR
            END
      END
      
      /*  ALTER COLUMN PARENT CODE OF "TESTELISTATRASF" FOR ALL CHILDREN IN "RIGHELISTATRASF"  */
      IF UPDATE(PROGRESSIVO)
      BEGIN
         UPDATE RIGHELISTATRASF
          SET   IDTESTA = I1.PROGRESSIVO
         FROM   RIGHELISTATRASF T2, INSERTED I1, DELETED D1
         WHERE  T2.IDTESTA = D1.PROGRESSIVO
          AND  (I1.PROGRESSIVO != D1.PROGRESSIVO)
      END

      RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

/*  DELETE TRIGGER "TD_TESTELISTATRASF" FOR TABLE "TESTELISTATRASF"  */
CREATE TRIGGER TD_TESTELISTATRASF ON TESTELISTATRASF FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  DELETE ALL CHILDREN IN "RIGHELISTATRASF"  */
    DELETE RIGHELISTATRASF
    FROM   RIGHELISTATRASF T2, DELETED T1
    WHERE  T2.IDTESTA = T1.PROGRESSIVO

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

