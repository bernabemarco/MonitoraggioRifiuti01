    [CODICELISTA]    VARCHAR (3)   NOT NULL,
    [NUMEROLISTA]    INT           NOT NULL,
    [DATAEMISSIONE]  DATETIME      NULL,
    [STATOCHIUSO]    SMALLINT      DEFAULT (0) NULL,
    [NOTE]           VARCHAR (100) NULL,
    [DATAMODIFICA]   DATETIME      NOT NULL,
    [UTENTEMODIFICA] VARCHAR (25)  NOT NULL,
    CONSTRAINT [PK_TESTELISTATRASF] PRIMARY KEY CLUSTERED ([PROGRESSIVO] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [CKC_STATOCHIUSO_TESTELISTRASF] CHECK ([STATOCHIUSO] = 0 or [STATOCHIUSO] = 1)
