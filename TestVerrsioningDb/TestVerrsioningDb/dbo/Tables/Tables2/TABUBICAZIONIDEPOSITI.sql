          SELECT @ERRNO  = 30006,
                 @ERRMSG = 'Children still exist in "UBICAZIONIARTICOLI". Cannot delete parent "TABUBICAZIONIDEPOSITI".'
          GOTO ERROR
       END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

