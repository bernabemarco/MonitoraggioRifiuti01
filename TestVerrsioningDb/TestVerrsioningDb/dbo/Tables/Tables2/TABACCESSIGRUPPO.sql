/*  INSERT TRIGGER "TI_TABACCESSIGRUPPO" FOR TABLE "TABACCESSIGRUPPO"  */
CREATE TRIGGER TI_TABACCESSIGRUPPO ON TABACCESSIGRUPPO FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "TABGRUPPIUTENTE" MUST EXIST WHEN INSERTING A CHILD IN "TABACCESSIGRUPPO"  */
    IF UPDATE(CODGRUPPO)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   TABGRUPPIUTENTE T1, INSERTED T2
           WHERE  T1.CODICE = T2.CODGRUPPO) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "TABGRUPPIUTENTE". Cannot create child in "TABACCESSIGRUPPO".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

