CREATE TRIGGER TU_TABSTATOVERSIONECICLO ON TABSTATOVERSIONECICLO FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      
      /*  CANNOT MODIFY PARENT CODE IN "TABSTATOVERSIONECICLO" IF CHILDREN STILL EXIST IN "TESTACICLOPROD"  */
      IF UPDATE(CODICE)
      BEGIN
         IF EXISTS (SELECT 1
                    FROM   TESTACICLOPROD T2, INSERTED I1, DELETED D1
                    WHERE  T2.STATOVERSIONE = D1.CODICE
                     AND  (I1.CODICE != D1.CODICE))
            BEGIN
               SELECT @ERRNO  = 30005,
                      @ERRMSG = 'Children still exist in "TESTACICLOPROD". Cannot modify parent code in "TABSTATOVERSIONECICLO".'
               GOTO ERROR
            END
      END

      RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

