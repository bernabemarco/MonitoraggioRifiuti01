CREATE TABLE [dbo].[TABTIPIUM] (
    [CODICE]         DECIMAL (5)  NOT NULL,
    [DESCRIZIONE]    VARCHAR (25) NULL,
    [UTENTEMODIFICA] VARCHAR (25) NOT NULL,
    [DATAMODIFICA]   DATETIME     NOT NULL,
    CONSTRAINT [PK_TABTIPIUM] PRIMARY KEY CLUSTERED ([CODICE] ASC) WITH (FILLFACTOR = 90)
);


GO
CREATE NONCLUSTERED INDEX [TABUM_DSC]
    ON [dbo].[TABTIPIUM]([DESCRIZIONE] ASC) WITH (FILLFACTOR = 90);


GO

/*  UPDATE TRIGGER "TU_TABTIPIUM" FOR TABLE "TABTIPIUM"  */
CREATE TRIGGER TU_TABTIPIUM ON TABTIPIUM FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      
      /*  CANNOT MODIFY PARENT CODE IN "TABTIPIUM" IF CHILDREN STILL EXIST IN "ARTICOLIUMPREFERITE"  */
      IF UPDATE(CODICE)
      BEGIN
         IF EXISTS (SELECT 1
                    FROM   ARTICOLIUMPREFERITE T2, INSERTED I1, DELETED D1
                    WHERE  T2.TIPOUM = D1.CODICE
                     AND  (I1.CODICE != D1.CODICE))
            BEGIN
               SELECT @ERRNO  = 30005,
                      @ERRMSG = 'Children still exist in "ARTICOLIUMPREFERITE". Cannot modify parent code in "TABTIPIUM".'
               GOTO ERROR
            END
      END

      RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

/*  DELETE TRIGGER "TD_TABTIPIUM" FOR TABLE "TABTIPIUM"  */
CREATE TRIGGER TD_TABTIPIUM ON TABTIPIUM FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  CANNOT DELETE PARENT "TABTIPIUM" IF CHILDREN STILL EXIST IN "ARTICOLIUMPREFERITE"  */
    IF EXISTS (SELECT 1
               FROM   ARTICOLIUMPREFERITE T2, DELETED T1
               WHERE  T2.TIPOUM = T1.CODICE)
       BEGIN
          SELECT @ERRNO  = 30006,
                 @ERRMSG = 'Children still exist in "ARTICOLIUMPREFERITE". Cannot delete parent "TABTIPIUM".'
          GOTO ERROR
       END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

