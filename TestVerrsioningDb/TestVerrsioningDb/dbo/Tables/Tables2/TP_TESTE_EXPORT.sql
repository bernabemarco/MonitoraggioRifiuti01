CREATE TABLE [dbo].[TP_TESTE_EXPORT] (
    [PROGRESSIVO]      DECIMAL (10)  NOT NULL,
    [DSCProfili]       VARCHAR (50)  NULL,
    [CodCli]           VARCHAR (7)   NULL,
    [TipoEsportazione] VARCHAR (16)  NULL,
    [Path]             VARCHAR (250) NULL,
    [PathCli]          VARCHAR (250) NULL,
    [UtenteModifica]   VARCHAR (25)  NOT NULL,
    [DataModifica]     DATETIME      NOT NULL,
    CONSTRAINT [PK_TP_TESTE_EXPORT] PRIMARY KEY CLUSTERED ([PROGRESSIVO] ASC) WITH (FILLFACTOR = 90)
);


GO

CREATE TRIGGER TD_TESTEEXPORT ON TP_TESTE_EXPORT FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN
  
    /*  DELETE ALL CHILDREN IN "TP_RIGHE_EXPORT"  */
    DELETE TP_RIGHE_EXPORT
    FROM   TP_RIGHE_EXPORT T2, DELETED T1
    WHERE  T2.RifProgressivo = T1.Progressivo

    /*  DELETE ALL CHILDREN IN "TP_DATI_EXPORT"  */
    DELETE TP_DATI_EXPORT
    FROM   TP_DATI_EXPORT T2, DELETED T1
    WHERE  T2.RifProgressivo = T1.Progressivo

    /*  DELETE ALL CHILDREN IN "TP_CONDIZIONI_EXPORT"  */
    DELETE TP_CONDIZIONI_EXPORT
    FROM   TP_CONDIZIONI_EXPORT T2, DELETED T1
    WHERE  T2.RifProgressivo = T1.Progressivo
     
    /*  DELETE ALL CHILDREN IN "TP_RELAZIONI_EXPORT"  */
    DELETE TP_RELAZIONI_EXPORT
    FROM   TP_RELAZIONI_EXPORT T2, DELETED T1
    WHERE  T2.RifProgressivo = T1.Progressivo

    /*  DELETE ALL CHILDREN IN "TP_LEGAMI_EXPORT"  */
    DELETE TP_LEGAMI_EXPORT
    FROM   TP_LEGAMI_EXPORT T2, DELETED T1
    WHERE  T2.RifProgressivo = T1.Progressivo

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END


GO
