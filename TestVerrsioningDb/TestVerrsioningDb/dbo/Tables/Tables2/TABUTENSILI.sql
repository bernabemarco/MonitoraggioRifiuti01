CREATE TABLE [dbo].[TABUTENSILI] (
    [CODICE]          VARCHAR (15)  NOT NULL,
    [DESCRIZIONE]     VARCHAR (80)  NULL,
    [CATEGORIA]       DECIMAL (5)   NOT NULL,
    [NOTE]            VARCHAR (100) NULL,
    [UTENTEMODIFICA]  VARCHAR (25)  NOT NULL,
    [DATAMODIFICA]    DATETIME      NOT NULL,
    [FLGVINCOLATO]    SMALLINT      NULL,
    [QTA_DISPONIBILE] DECIMAL (9)   NULL,
    CONSTRAINT [PK_TABUTENSILI] PRIMARY KEY CLUSTERED ([CODICE] ASC) WITH (FILLFACTOR = 90)
);


GO

/*  INSERT TRIGGER "TI_TABUTENSILI" FOR TABLE "TABUTENSILI"  */
CREATE TRIGGER TI_TABUTENSILI ON TABUTENSILI FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "TABCATEGORIEUTENSILI" MUST EXIST WHEN INSERTING A CHILD IN "TABUTENSILI"  */
    IF UPDATE(CATEGORIA)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   TABCATEGORIEUTENSILI T1, INSERTED T2
           WHERE  T1.CODICE = T2.CATEGORIA) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "TABCATEGORIEUTENSILI". Cannot create child in "TABUTENSILI".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

/*  UPDATE TRIGGER "TU_TABUTENSILI" FOR TABLE "TABUTENSILI"  */
CREATE TRIGGER TU_TABUTENSILI ON TABUTENSILI FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      
      /*  PARENT "TABCATEGORIEUTENSILI" MUST EXIST WHEN UPDATING A CHILD IN "TABUTENSILI"  */
      IF UPDATE(CATEGORIA)
      BEGIN
         IF (SELECT COUNT(*)
             FROM   TABCATEGORIEUTENSILI T1, INSERTED T2
             WHERE  T1.CODICE = T2.CATEGORIA) != @NUMROWS
            BEGIN
               SELECT @ERRNO  = 30003,
                      @ERRMSG = '"TABCATEGORIEUTENSILI" does not exist. Cannot ALTER COLUMN child in "TABUTENSILI".'
               GOTO ERROR
            END
      END
      
      /*  ALTER COLUMN PARENT CODE OF "TABUTENSILI" FOR ALL CHILDREN IN "ANACARUTENSILI"  */
      IF UPDATE(CODICE)
      BEGIN
         UPDATE ANACARUTENSILI
          SET   UTENSILE = I1.CODICE
         FROM   ANACARUTENSILI T2, INSERTED I1, DELETED D1
         WHERE  T2.UTENSILE = D1.CODICE
          AND  (I1.CODICE != D1.CODICE)
      END

      RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

/*  DELETE TRIGGER "TD_TABUTENSILI" FOR TABLE "TABUTENSILI"  */
CREATE TRIGGER TD_TABUTENSILI ON TABUTENSILI FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  DELETE ALL CHILDREN IN "ANACARUTENSILI"  */
    DELETE ANACARUTENSILI
    FROM   ANACARUTENSILI T2, DELETED T1
    WHERE  T2.UTENSILE = T1.CODICE

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO
