CREATE TABLE [dbo].[TABLISTINITRASFORMAZIONE] (
    [NRLISTINO]      DECIMAL (5)  NOT NULL,
    [DESCRIZIONE]    VARCHAR (80) NULL,
    [CODCAMBIO]      DECIMAL (5)  NULL,
    [VINCOLAUM]      SMALLINT     DEFAULT (0) NULL,
    [UTENTEMODIFICA] VARCHAR (25) NOT NULL,
    [DATAMODIFICA]   DATETIME     NOT NULL,
    CONSTRAINT [PK_TABLISTINITRASFORMAZIONE] PRIMARY KEY CLUSTERED ([NRLISTINO] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [CKC_VINCOLAUM_TABLISTITRASF] CHECK ([VINCOLAUM] = 0 or [VINCOLAUM] = 1)
