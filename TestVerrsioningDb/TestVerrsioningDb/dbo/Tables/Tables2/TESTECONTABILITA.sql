CREATE TRIGGER TU_TESTECONTABILITA ON TESTECONTABILITA FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      /*  MODIFY PARENT CODE OF "TESTECONTABILITA" FOR ALL CHILDREN IN "MOVIMENTICDC"  */
      IF UPDATE(PROGRESSIVO)
      BEGIN
         UPDATE MOVIMENTICDC
          SET   IDRIFERIMENTO = I1.PROGRESSIVO
         FROM   MOVIMENTICDC T2, INSERTED I1, DELETED D1
         WHERE  T2.IDRIFERIMENTO = D1.PROGRESSIVO
          AND  (I1.PROGRESSIVO != D1.PROGRESSIVO) AND T2.TIPOMOV=1
      END

      RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END



GO
CREATE TRIGGER TD_TESTECONTABILITA ON TESTECONTABILITA FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    /*  DELETE ALL CHILDREN IN "MOVIMENTICDC"  */
    DELETE MOVIMENTICDC
    FROM   MOVIMENTICDC T2, DELETED T1
    WHERE  T2.IDRIFERIMENTO = T1.PROGRESSIVO AND T2.TIPOMOV=1

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

