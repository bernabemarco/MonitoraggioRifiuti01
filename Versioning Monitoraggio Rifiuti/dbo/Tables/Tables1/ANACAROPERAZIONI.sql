             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "TABELLAOPERAZIONI". Cannot create child in "ANACAROPERAZIONI".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

