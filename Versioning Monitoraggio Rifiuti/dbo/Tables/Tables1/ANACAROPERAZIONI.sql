/*  INSERT TRIGGER "TI_ANACAROPERAZIONI" FOR TABLE "ANACAROPERAZIONI"  */
CREATE TRIGGER TI_ANACAROPERAZIONI ON ANACAROPERAZIONI FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "TABELLAOPERAZIONI" MUST EXIST WHEN INSERTING A CHILD IN "ANACAROPERAZIONI"  */
    IF UPDATE(OPERAZIONE)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   TABELLAOPERAZIONI T1, INSERTED T2
           WHERE  T1.CODICE = T2.OPERAZIONE) != @NUMROWS
