/*  INSERT TRIGGER "TI_CONTROPARTARTICOLI" FOR TABLE "CONTROPARTARTICOLI"  */
CREATE TRIGGER TI_CONTROPARTARTICOLI ON CONTROPARTARTICOLI FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "ANAGRAFICAARTICOLICOMM" MUST EXIST WHEN INSERTING A CHILD IN "CONTROPARTARTICOLI"  */
    IF UPDATE(CODART) OR
       UPDATE(ESERCIZIO)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   ANAGRAFICAARTICOLICOMM T1, INSERTED T2
           WHERE  T1.CODICEART = T2.CODART
            AND   T1.ESERCIZIO = T2.ESERCIZIO) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "ANAGRAFICAARTICOLICOMM". Cannot create child in "CONTROPARTARTICOLI".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

