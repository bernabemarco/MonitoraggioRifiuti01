CREATE TABLE [dbo].[DESCRARTICOLI] (
    [CODICEART]       VARCHAR (50)  NOT NULL,
    [LINGUA]          DECIMAL (5)   NOT NULL,
    [DESCRIZIONEAGG]  VARCHAR (255) NULL,
    [INFORMAZIONIAGG] VARCHAR (255) NULL,
    [UTENTEMODIFICA]  VARCHAR (25)  NOT NULL,
    [DATAMODIFICA]    DATETIME      NOT NULL,
    CONSTRAINT [PK_DESCRARTICOLI] PRIMARY KEY CLUSTERED ([CODICEART] ASC, [LINGUA] ASC) WITH (FILLFACTOR = 90)
);


GO
