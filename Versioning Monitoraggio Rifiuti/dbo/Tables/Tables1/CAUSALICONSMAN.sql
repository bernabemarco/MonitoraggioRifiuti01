GO

/*  UPDATE TRIGGER "TU_CAUSALICONSMAN" FOR TABLE "CAUSALICONSMAN"  */
CREATE TRIGGER TU_CAUSALICONSMAN ON CAUSALICONSMAN FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      
      /*  CANNOT MODIFY PARENT CODE IN "CAUSALICONSMAN" IF CHILDREN STILL EXIST IN "STORICOCONSMANCOMM"  */
      IF UPDATE(CODICE)
      BEGIN
         IF EXISTS (SELECT 1
                    FROM   STORICOCONSMANCOMM T2, INSERTED I1, DELETED D1
                    WHERE  T2.CAUSALE = D1.CODICE
                     AND  (I1.CODICE != D1.CODICE))
            BEGIN
               SELECT @ERRNO  = 30005,
                      @ERRMSG = 'Children still exist in "STORICOCONSMANCOMM". Cannot modify parent code in "CAUSALICONSMAN".'
               GOTO ERROR
            END
      END

      RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

/*  DELETE TRIGGER "TD_CAUSALICONSMAN" FOR TABLE "CAUSALICONSMAN"  */
CREATE TRIGGER TD_CAUSALICONSMAN ON CAUSALICONSMAN FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  CANNOT DELETE PARENT "CAUSALICONSMAN" IF CHILDREN STILL EXIST IN "STORICOCONSMANCOMM"  */
    IF EXISTS (SELECT 1
               FROM   STORICOCONSMANCOMM T2, DELETED T1
               WHERE  T2.CAUSALE = T1.CODICE)
       BEGIN
          SELECT @ERRNO  = 30006,
                 @ERRMSG = 'Children still exist in "STORICOCONSMANCOMM". Cannot delete parent "CAUSALICONSMAN".'
          GOTO ERROR
       END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

