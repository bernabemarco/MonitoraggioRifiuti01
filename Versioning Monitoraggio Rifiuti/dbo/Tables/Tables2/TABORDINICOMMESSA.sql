CREATE TABLE [dbo].[TABORDINICOMMESSA] (
    [CODCOMMESSA]    VARCHAR (3)  NOT NULL,
    [CODORDINE]      VARCHAR (3)  NOT NULL,
    [POSIZIONE]      DECIMAL (10) NULL,
    [DATAMODIFICA]   DATETIME     NOT NULL,
    [UTENTEMODIFICA] VARCHAR (25) NOT NULL,
    CONSTRAINT [PK_TABORDINICOMMESSA] PRIMARY KEY CLUSTERED ([CODCOMMESSA] ASC, [CODORDINE] ASC) WITH (FILLFACTOR = 90)
);


GO

/*  INSERT TRIGGER "TI_TABORDINICOMMESSA" FOR TABLE "TABORDINICOMMESSA"  */
CREATE TRIGGER TI_TABORDINICOMMESSA ON TABORDINICOMMESSA FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "PARAMETRICOMMPROD" MUST EXIST WHEN INSERTING A CHILD IN "TABORDINICOMMESSA"  */
    IF UPDATE(CODCOMMESSA)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   PARAMETRICOMMPROD T1, INSERTED T2
           WHERE  T1.CODICE = T2.CODCOMMESSA) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "PARAMETRICOMMPROD". Cannot create child in "TABORDINICOMMESSA".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

