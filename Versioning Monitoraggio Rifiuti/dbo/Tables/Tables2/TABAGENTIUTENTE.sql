CREATE TABLE [dbo].[TABAGENTIUTENTE] (
    [CODUTENTE]      VARCHAR (25)  NOT NULL,
    [FORMID]         INT           DEFAULT (0) NOT NULL,
    [AGENTE]         VARCHAR (128) DEFAULT ('') NULL,
    [ESEGUIGRUPPO]   SMALLINT      DEFAULT (0) NULL,
    [UTENTEMODIFICA] VARCHAR (25)  NOT NULL,
    [DATAMODIFICA]   DATETIME      NOT NULL,
    CONSTRAINT [PK_TABAGENTIUTENTE] PRIMARY KEY CLUSTERED ([CODUTENTE] ASC, [FORMID] ASC) WITH (FILLFACTOR = 90),
