/*  INSERT TRIGGER "TI_TABAGENTIUTENTE" FOR TABLE "TABAGENTIUTENTE"  */
CREATE TRIGGER TI_TABAGENTIUTENTE ON TABAGENTIUTENTE FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "TABUTENTI" MUST EXIST WHEN INSERTING A CHILD IN "TABAGENTIUTENTE"  */
    IF UPDATE(CODUTENTE)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   TABUTENTI T1, INSERTED T2
           WHERE  T1.USERID = T2.CODUTENTE) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "TABUTENTI". Cannot create child in "TABAGENTIUTENTE".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

