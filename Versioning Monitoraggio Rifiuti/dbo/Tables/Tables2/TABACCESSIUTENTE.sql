/*  UPDATE TRIGGER "TU_TABACCESSIUTENTE" FOR TABLE "TABACCESSIUTENTE"  */
CREATE TRIGGER TU_TABACCESSIUTENTE ON TABACCESSIUTENTE FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      
      /*  PARENT "TABUTENTI" MUST EXIST WHEN UPDATING A CHILD IN "TABACCESSIUTENTE"  */
