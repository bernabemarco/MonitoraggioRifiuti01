               SELECT @ERRNO  = 30003,
                      @ERRMSG = '"TABUTENTI" does not exist. Cannot modify child in "TABACCESSIUTENTE".'
               GOTO ERROR
            END
      END

      RETURN

/*  ERRORS HANDLING  */
