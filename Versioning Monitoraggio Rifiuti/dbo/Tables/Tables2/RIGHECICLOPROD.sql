/*  DELETE TRIGGER "TD_RIGHECICLOPROD" FOR TABLE "RIGHECICLOPROD"  */
CREATE TRIGGER TD_RIGHECICLOPROD ON RIGHECICLOPROD FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  DELETE ALL CHILDREN IN "TABFORNITORICICLO"  */
    DELETE TABFORNITORICICLO
    FROM   TABFORNITORICICLO T2, DELETED T1
    WHERE  T2.PROGRESSIVO = T1.PROGRESSIVO
     AND   T2.NUMEROFASE = T1.NUMEROFASE
    
    /*  DELETE ALL CHILDREN IN "TABUTENSILICICLO"  */
    DELETE TABUTENSILICICLO
    FROM   TABUTENSILICICLO T2, DELETED T1
    WHERE  T2.PROGRESSIVO = T1.PROGRESSIVO
     AND   T2.NUMEROFASE = T1.NUMEROFASE
    
    /*  DELETE ALL CHILDREN IN "CAROPERAZIONEFASE"  */
    DELETE CAROPERAZIONEFASE
    FROM   CAROPERAZIONEFASE T2, DELETED T1
    WHERE  T2.PROGRESSIVO = T1.PROGRESSIVO
     AND   T2.NUMEROFASE = T1.NUMEROFASE
    
    /*  DELETE ALL CHILDREN IN "TABCOMPONENTIDBACICLO"  */
