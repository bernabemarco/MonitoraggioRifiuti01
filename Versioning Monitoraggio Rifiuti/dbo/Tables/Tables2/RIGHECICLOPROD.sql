    FROM   TABCOMPONENTIDBACICLO T2, DELETED T1
    WHERE  T2.PROGRESSIVO = T1.PROGRESSIVO
     AND   T2.NUMEROFASE = T1.NUMEROFASE

    RETURN

/*  ERRORS HANDLING  */
ERROR:
