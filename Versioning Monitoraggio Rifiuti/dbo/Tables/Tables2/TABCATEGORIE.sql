/*  UPDATE TRIGGER "TU_TABCATEGORIE" FOR TABLE "TABCATEGORIE"  */
CREATE TRIGGER TU_TABCATEGORIE ON TABCATEGORIE FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      
      /*  MODIFY PARENT CODE OF "TABCATEGORIE" FOR ALL CHILDREN IN "TABCARATTLOTTI"  */
      IF UPDATE(CODICE)
      BEGIN
         UPDATE TABCARATTLOTTI
          SET   CODICE = I1.CODICE
         FROM   TABCARATTLOTTI T2, INSERTED I1, DELETED D1
         WHERE  T2.CODICE = D1.CODICE
          AND  (I1.CODICE != D1.CODICE)
      END

      RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

/*  DELETE TRIGGER "TD_TABCATEGORIE" FOR TABLE "TABCATEGORIE"  */
CREATE TRIGGER TD_TABCATEGORIE ON TABCATEGORIE FOR DELETE AS
BEGIN
    DECLARE
       @NUMROWS  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  DELETE ALL CHILDREN IN "TABCARATTLOTTI"  */
    DELETE TABCARATTLOTTI
    FROM   TABCARATTLOTTI T2, DELETED T1
    WHERE  T2.CODICE = T1.CODICE

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

