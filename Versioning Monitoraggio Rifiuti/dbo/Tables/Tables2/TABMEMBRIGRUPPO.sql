CREATE TABLE [dbo].[TABMEMBRIGRUPPO] (
    [CODGRUPPO]      DECIMAL (5)  NOT NULL,
    [CODUTENTE]      VARCHAR (25) NOT NULL,
    [UTENTEMODIFICA] VARCHAR (25) NOT NULL,
    [DATAMODIFICA]   DATETIME     NOT NULL,
    CONSTRAINT [PK_TABMEMBRIGRUPPO] PRIMARY KEY CLUSTERED ([CODGRUPPO] ASC, [CODUTENTE] ASC) WITH (FILLFACTOR = 90)
);


GO

/*  INSERT TRIGGER "TI_TABMEMBRIGRUPPO" FOR TABLE "TABMEMBRIGRUPPO"  */
CREATE TRIGGER TI_TABMEMBRIGRUPPO ON TABMEMBRIGRUPPO FOR INSERT AS
BEGIN
    DECLARE
       @MAXCARD  INT,
       @NUMROWS  INT,
       @NUMNULL  INT,
       @ERRNO    INT,
       @ERRMSG   VARCHAR(255)

    SELECT  @NUMROWS = @@ROWCOUNT
    IF @NUMROWS = 0
       RETURN

    
    /*  PARENT "TABGRUPPIUTENTE" MUST EXIST WHEN INSERTING A CHILD IN "TABMEMBRIGRUPPO"  */
    IF UPDATE(CODGRUPPO)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   TABGRUPPIUTENTE T1, INSERTED T2
           WHERE  T1.CODICE = T2.CODGRUPPO) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "TABGRUPPIUTENTE". Cannot create child in "TABMEMBRIGRUPPO".'
             GOTO ERROR
          END
    END
    
    /*  PARENT "TABUTENTI" MUST EXIST WHEN INSERTING A CHILD IN "TABMEMBRIGRUPPO"  */
    IF UPDATE(CODUTENTE)
    BEGIN
       IF (SELECT COUNT(*)
           FROM   TABUTENTI T1, INSERTED T2
           WHERE  T1.USERID = T2.CODUTENTE) != @NUMROWS
          BEGIN
             SELECT @ERRNO  = 30002,
                    @ERRMSG = 'Parent does not exist in "TABUTENTI". Cannot create child in "TABMEMBRIGRUPPO".'
             GOTO ERROR
          END
    END

    RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

GO

