/*  UPDATE TRIGGER "TU_STORICOMOVORDPROD" FOR TABLE "STORICOMOVORDPROD"  */
CREATE TRIGGER TU_STORICOMOVORDPROD ON STORICOMOVORDPROD FOR UPDATE AS
BEGIN
   DECLARE
      @MAXCARD  INT,
      @NUMROWS  INT,
      @NUMNULL  INT,
      @ERRNO    INT,
      @ERRMSG   VARCHAR(255)

      SELECT  @NUMROWS = @@ROWCOUNT
      IF @NUMROWS = 0
         RETURN

      
      /*  PARENT "STORICOMOVPROD" MUST EXIST WHEN UPDATING A CHILD IN "STORICOMOVORDPROD"  */
      IF UPDATE(RIFPROGRESSIVO)
      BEGIN
         IF (SELECT COUNT(*)
             FROM   STORICOMOVPROD T1, INSERTED T2
             WHERE  T1.PROGRESSIVO = T2.RIFPROGRESSIVO) != @NUMROWS
            BEGIN
               SELECT @ERRNO  = 30003,
                      @ERRMSG = '"STORICOMOVPROD" does not exist. Cannot ALTER COLUMN child in "STORICOMOVORDPROD".'
               GOTO ERROR
            END
      END
      
      /*  ALTER COLUMN PARENT CODE OF "STORICOMOVORDPROD" FOR ALL CHILDREN IN "STORICOMOVORDPRODMATRICOLE"  */
      IF UPDATE(RIFPROGRESSIVO) OR
         UPDATE(RIGAMOVORD)
      BEGIN
         UPDATE STORICOMOVORDPRODMATRICOLE
          SET   RIFPROGRESSIVO = I1.RIFPROGRESSIVO,
                RIFRIGAMOVORD = I1.RIGAMOVORD
         FROM   STORICOMOVORDPRODMATRICOLE T2, INSERTED I1, DELETED D1
         WHERE  T2.RIFPROGRESSIVO = D1.RIFPROGRESSIVO
          AND   T2.RIFRIGAMOVORD = D1.RIGAMOVORD
          AND  (I1.RIFPROGRESSIVO != D1.RIFPROGRESSIVO OR
                I1.RIGAMOVORD != D1.RIGAMOVORD)
      END
      
      /*  ALTER COLUMN PARENT CODE OF "STORICOMOVORDPROD" FOR ALL CHILDREN IN "STORICOMOVIMPPROD"  */
      IF UPDATE(RIFPROGRESSIVO) OR
         UPDATE(RIGAMOVORD)
      BEGIN
         UPDATE STORICOMOVIMPPROD
          SET   RIFPROGRESSIVO = I1.RIFPROGRESSIVO,
                RIGAMOVORD = I1.RIGAMOVORD
         FROM   STORICOMOVIMPPROD T2, INSERTED I1, DELETED D1
         WHERE  T2.RIFPROGRESSIVO = D1.RIFPROGRESSIVO
          AND   T2.RIGAMOVORD = D1.RIGAMOVORD
          AND  (I1.RIFPROGRESSIVO != D1.RIFPROGRESSIVO OR
                I1.RIGAMOVORD != D1.RIGAMOVORD)
      END

      RETURN

/*  ERRORS HANDLING  */
ERROR:
    RAISERROR (@ERRMSG, 1, 1)
    ROLLBACK  TRANSACTION
END

