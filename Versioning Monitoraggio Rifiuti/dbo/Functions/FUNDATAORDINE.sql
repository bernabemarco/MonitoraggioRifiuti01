
CREATE FUNCTION [dbo].[FUNDATAORDINE](@PROGRESSIVORP DECIMAL(10,0),@IDRIGARP INT)
RETURNS DATETIME
AS
BEGIN

DECLARE @DATAORDINE DATETIME
	
	SET @DATAORDINE = ''

	SELECT @DATAORDINE=DATADOC FROM TESTEDOCUMENTI 
	WHERE PROGRESSIVO=(SELECT IDTESTARP FROM RIGHEDOCUMENTI WHERE IDTESTA=@PROGRESSIVORP AND IDRIGA=@IDRIGARP)

	RETURN(@DATAORDINE)
END

GO
GRANT EXECUTE
    ON OBJECT::[dbo].[FUNDATAORDINE] TO [Metodo98]
    AS [dbo];

