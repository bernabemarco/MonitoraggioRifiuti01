
CREATE VIEW TABCFG AS
    SELECT 
        CODCONTO,
        DSCCONTO1,
        CODMASTRO,
        PARTITAIVA,
        CODFISCALE,
        TIPOCONTO,
        TIPO AS TIPOMASTRO,
        DESCRIZIONE AS DSCMASTRO,
        FLGDOGANA
    FROM 
       ANAGRAFICACF INNER JOIN TABMASTRI ON TABMASTRI.CODICE = ANAGRAFICACF.CODMASTRO
    UNION ALL
    SELECT 
        CODCONTO,
        DSCCONTO1,
        CODMASTRO,
        '',
        '',
        'G',
        TIPO AS TIPOMASTRO,
        DESCRIZIONE AS DSCMASTRO,
        0 AS FLGDOGANA
    FROM 
       ANAGRAFICAGENERICI INNER JOIN TABMASTRI ON TABMASTRI.CODICE = ANAGRAFICAGENERICI.CODMASTRO
GO
GRANT SELECT
    ON OBJECT::[dbo].[TABCFG] TO [Metodo98]
    AS [dbo];

