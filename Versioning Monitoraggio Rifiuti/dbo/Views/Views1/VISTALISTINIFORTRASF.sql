CREATE VIEW VISTALISTINIFORTRASF AS
	SELECT 
		TBFT.NRLISTINO, 
		(SELECT DESCRIZIONE FROM TabListiniTrasformazione  TL WHERE TL.NRLISTINO=TBFT.NRLISTINO) AS DESCRIZIONE,
		TBFT.CODFOR,
		(SELECT DSCCONTO1 FROM ANAGRAFICACF CF WHERE CF.CODCONTO=TBFT.CODFOR) AS DSCCONTO1
	FROM
		TABLISTINIFORTRASF TBFT

GO
GRANT SELECT
    ON OBJECT::[dbo].[VISTALISTINIFORTRASF] TO [Metodo98]
    AS [dbo];

