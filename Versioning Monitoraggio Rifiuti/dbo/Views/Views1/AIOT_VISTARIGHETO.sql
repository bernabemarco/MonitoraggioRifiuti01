
CREATE VIEW AIOT_VISTARIGHETO
AS

SELECT     dbo.ACQ_RIGHEOFFERTA.RIFPROGRESSIVO, dbo.ACQ_RIGHEOFFERTA.IDRIGA, dbo.ACQ_RIGHEOFFERTA.ARTICOLO, dbo.ANAGRAFICAARTICOLI.DESCRIZIONE, 
                      dbo.ACQ_RIGHEOFFERTA.UM, dbo.ACQ_RIGHEOFFERTA.QTAMINIMA, dbo.ACQ_RIGHEOFFERTA.PREZZO, dbo.ACQ_RIGHEOFFERTA.UTENTEMODIFICA, 
                      dbo.ACQ_RIGHEOFFERTA.DATAMODIFICA
FROM         dbo.ACQ_RIGHEOFFERTA LEFT OUTER JOIN
                      dbo.ANAGRAFICAARTICOLI ON dbo.ACQ_RIGHEOFFERTA.ARTICOLO = dbo.ANAGRAFICAARTICOLI.CODICE


