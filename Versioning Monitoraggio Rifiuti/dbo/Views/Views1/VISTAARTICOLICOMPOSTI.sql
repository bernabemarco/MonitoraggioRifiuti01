create view
  VISTAARTICOLICOMPOSTI
  as select TDBA.PROGRESSIVO,
    TDBA.IDENTIFICATIVODBA,
    TDBA.ARTCOMPOSTO,
    TDBA.VERSIONEDBA,
    TDBA.DSCVERSIONE,
    TDBA.STATOVERSIONE,
    TDBA.VALIDITADBA,
    TDBA.DBASTANDARD,
    TDBA.NOTEDBA,
    TDBA.UM,
    TDBA.QUANTITARIF,
    TDBA.RIFARTICOLO,
    ART.DESCRIZIONE as DSCARTCOMPOSTO,
    STSV.DESCRIZIONE as DSCSTATOVERSIONE,
    STSV.USAVERSIONE
  from 
   	((DISTINTAARTCOMPOSTI as TDBA left outer join ANAGRAFICAARTICOLI ART
   	on TDBA.RIFARTICOLO=ART.CODICE) 
   	join TABSTATOVERSIONEDBA as STSV on TDBA.STATOVERSIONE=STSV.CODICE)

GO
GRANT SELECT
    ON OBJECT::[dbo].[VISTAARTICOLICOMPOSTI] TO [Metodo98]
    AS [dbo];

