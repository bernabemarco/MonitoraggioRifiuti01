CREATE VIEW MATRICOLEINGIACENZA AS
SELECT 
	SM.CODART,
    SM.NRIFPARTITA,
	SMM.CODMATRICOLA,
	SUM(((CASE WHEN SM.GIACENZA=1 and SM.RESO = 0 THEN 1 ELSE 0 END)-
         (CASE WHEN SM.GIACENZA=-1 and SM.RESO = 0 THEN 1 ELSE 0 END)+
         (CASE WHEN SM.GIACENZA=1 and SM.RESO = 1 THEN 1 ELSE 0 END)-
         (CASE WHEN SM.GIACENZA=-1 and SM.RESO = 1 THEN 1 ELSE 0 END))) AS QUANTITA
FROM 
	STORICOMAGMATRICOLE SMM INNER JOIN STORICOMAG SM ON SMM.PROGRESSIVO=SM.PROGRESSIVO
GROUP BY 
	SM.CODART,
	SM.NRIFPARTITA,
	SMM.CODMATRICOLA
HAVING
	SUM(((CASE WHEN SM.GIACENZA=1 and SM.RESO = 0 THEN 1 ELSE 0 END)-
         (CASE WHEN SM.GIACENZA=-1 and SM.RESO = 0 THEN 1 ELSE 0 END)+
         (CASE WHEN SM.GIACENZA=1 and SM.RESO = 1 THEN 1 ELSE 0 END)-
         (CASE WHEN SM.GIACENZA=-1 and SM.RESO = 1 THEN 1 ELSE 0 END)))=1

GO
GRANT SELECT
    ON OBJECT::[dbo].[MATRICOLEINGIACENZA] TO [Metodo98]
    AS [dbo];

