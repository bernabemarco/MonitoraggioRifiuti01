CREATE VIEW  Ald_StoricoModificheDataDoc
AS 
SELECT     REPLACE(REPLACE(STORICOMODIFICHECAMPI.CODICE, 'PROGRESSIVO= ', ''), '1=1 AND ', '') AS IdTesta, STORICOMODIFICHECAMPI.PROGRESSIVO, 
                      STORICOMODIFICHECAMPI.NOMETABELLA, STORICOMODIFICHECAMPI.NOMECAMPO, STORICOMODIFICHECAMPI.CODICE, STORICOMODIFICHECAMPI.VALORE, 
                      STORICOMODIFICHECAMPI.UTENTEMODIFICA, STORICOMODIFICHECAMPI.DATAMODIFICA, TABUTENTI.DESCRIZIONE
FROM         STORICOMODIFICHECAMPI INNER JOIN
                      TABUTENTI ON STORICOMODIFICHECAMPI.UTENTEMODIFICA = TABUTENTI.USERID
where NOMETABELLA='TESTEDOCUMENTI' AND NOMECAMPO='DATADOC'

GO
GRANT SELECT
    ON OBJECT::[dbo].[Ald_StoricoModificheDataDoc] TO [Metodo98]
    AS [dbo];

