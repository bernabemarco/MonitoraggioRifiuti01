CREATE VIEW [dbo].[TP_VISTASCTLOGSCATVAL]
AS
SELECT     TP_TESTE_SCONTI_LOGISTICI.PROGRESSIVO, TP_TESTE_SCONTI_LOGISTICI.DADATA, TP_TESTE_SCONTI_LOGISTICI.ADATA, 
                      TP_TESTE_SCONTI_LOGISTICI.CODCLIFOR, TP_TESTE_SCONTI_LOGISTICI.TIPOLOGIADETTAGLIO,TP_TESTE_SCONTI_LOGISTICI.TIPOLOGIASCONTO,
                      TP_TESTE_SCONTI_LOGISTICI.TIPO_TOTALE,TP_TESTE_SCONTI_LOGISTICI.RifContratto,
                      ANAGRAFICAARTICOLI.CODICE, 
                      TP_RIGHE_SCONTI_LOGISTICI_CATEGORIASTAT.UM, TP_RIGHE_SCONTI_LOGISTICI_CATEGORIASTAT.VALORE, 
                      TP_RIGHE_SCONTI_LOGISTICI_CATEGORIASTAT.SCONTO
FROM         TP_TESTE_SCONTI_LOGISTICI INNER JOIN
                      TP_RIGHE_SCONTI_LOGISTICI_CATEGORIASTAT ON 
                      TP_TESTE_SCONTI_LOGISTICI.PROGRESSIVO = TP_RIGHE_SCONTI_LOGISTICI_CATEGORIASTAT.RIFPROGRESSIVO INNER JOIN
                      ANAGRAFICAARTICOLI ON 
                      TP_RIGHE_SCONTI_LOGISTICI_CATEGORIASTAT.CATEGORIASTAT = ANAGRAFICAARTICOLI.CODCATEGORIASTAT
WHERE     (TP_TESTE_SCONTI_LOGISTICI.TIPOLOGIADETTAGLIO = 2) AND (ANAGRAFICAARTICOLI.CODCATEGORIASTAT <> 0)

GO
