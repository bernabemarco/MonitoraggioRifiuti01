CREATE VIEW [dbo].[GEM_VISTA_Sezioni_MezziAttivi]
AS
SELECT           dbo.GEM_SEZIONICONTRATTO.IDCONTRATTO, dbo.GEM_SEZIONICONTRATTO.SEZIONECONTRATTO, 
        COUNT(dbo.GEM_SEZIONICONTRATTODETTMEZZI.IdRigaMezzo) AS NrMezziAttivi, 
                        SUM(isnull(dbo.GEM_SEZIONICONTRATTODETTMEZZI.IMPORTOAT1,0)) AS IMPORTOAT1
FROM  dbo.GEM_SEZIONICONTRATTO JOIN dbo.GEM_SEZIONICONTRATTODETTMEZZI ON 
                        (dbo.GEM_SEZIONICONTRATTO.IDCONTRATTO = dbo.GEM_SEZIONICONTRATTODETTMEZZI.IDCONTRATTO AND 
        dbo.GEM_SEZIONICONTRATTO.SEZIONECONTRATTO = dbo.GEM_SEZIONICONTRATTODETTMEZZI.SEZIONECONTRATTO)
WHERE           
--GEM_SEZIONICONTRATTO.IDCONTRATTO='2010-000236SI' and
--GEM_SEZIONICONTRATTO.SEZIONECONTRATTO=6 and
(ISNULL(dbo.GEM_SEZIONICONTRATTODETTMEZZI.ELIMINATO, 0) = 0)
GROUP BY dbo.GEM_SEZIONICONTRATTO.IDCONTRATTO, dbo.GEM_SEZIONICONTRATTO.SEZIONECONTRATTO


GO
