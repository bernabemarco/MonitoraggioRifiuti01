

CREATE VIEW [dbo].[ALDEBRA_ADI_MANCANTIADIUTO_PASSIVO] AS
/*SELEZIONE DOCUMENTI MANCANTI NELLA FAMIGLIA A1005*/
SELECT AVD.* 
FROM 
ALDEBRA_VISTADOCUMENTALE AVD LEFT OUTER JOIN
Sicura_DATADB.dbo.A1005 FAMDOC ON AVD.KEYUNIQUE=FAMDOC.F1001
--	INNER JOIN  Sicura_DATADB.dbo.TDOC ON FAMDOC.FIDD = TDOC.FIDD
WHERE 
/*VIENE ELIMINATO DALLA SELEZIONE TUTTO CIO' CHE E' PRECEDENTE AL 2013*/
AVD.ESERCIZIODOC>=2016 AND
/*VENGONO ELIMINATE LE FATTURE ACCOMPAGNATORIE NON APPARTENENTI ALLA FAM A1005*/
AVD.KEYUNIQUE LIKE 'FA2-%' AND
FAMDOC.F1001 IS NULL
--and TDOC.FENA=-1
union all
SELECT AVD.* 
FROM 
ALDEBRA_VISTADOCUMENTALE AVD LEFT OUTER JOIN
Sicura_DATADB.dbo.A1023 FAMDOC ON AVD.KEYUNIQUE=FAMDOC.F1001
--	INNER JOIN  Sicura_DATADB.dbo.TDOC ON FAMDOC.FIDD = TDOC.FIDD
WHERE 
/*VIENE ELIMINATO DALLA SELEZIONE TUTTO CIO' CHE E' PRECEDENTE AL 2013*/
AVD.ESERCIZIODOC>=2016 AND
/*VENGONO ELIMINATE LE FATTURE ACCOMPAGNATORIE NON APPARTENENTI ALLA FAM A1005*/
AVD.KEYUNIQUE LIKE 'FT2-%' AND
FAMDOC.F1001 IS NULL
--and TDOC.FENA=-1
union all
SELECT AVD.* 
FROM 
ALDEBRA_VISTADOCUMENTALE AVD LEFT OUTER JOIN
Sicura_DATADB.dbo.A1024 FAMDOC ON AVD.KEYUNIQUE=FAMDOC.F1001
--	INNER JOIN  Sicura_DATADB.dbo.TDOC ON FAMDOC.FIDD = TDOC.FIDD
WHERE 
/*VIENE ELIMINATO DALLA SELEZIONE TUTTO CIO' CHE E' PRECEDENTE AL 2013*/
AVD.ESERCIZIODOC>=2016 AND
/*VENGONO ELIMINATE LE FATTURE ACCOMPAGNATORIE NON APPARTENENTI ALLA FAM A1005*/
AVD.KEYUNIQUE LIKE 'FR2-%' AND
FAMDOC.F1001 IS NULL
--and TDOC.FENA=-1




GO
