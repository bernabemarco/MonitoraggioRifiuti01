
CREATE VIEW VISTAPRODOTTIVARIANTI AS SELECT
	CATALOGOPRODOTTI.ID_PRODOTTO as prodotto,
	(CASE WHEN CATALOGOPRODOTTI.PUBBLICA=0 THEN 'No'  ELSE 'Sì' END)AS SELVARIANTI
FROM
	(CATALOGOPRODOTTI INNER JOIN TABVARIANTIECOM
	ON TABVARIANTIECOM.CODPADRE=CATALOGOPRODOTTI.ID_PRODOTTO)

GO
